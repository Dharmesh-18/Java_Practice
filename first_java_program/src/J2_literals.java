public class J2_literals
{
    public static void main(String[] args)
    {
        byte age = 50;
        float money = 50.7f;
        System.out.println("age: "+ age);
        System.out.println("money : $" + money);
        String name = "Dharmesh";
        System.out.println("Name is : "+ name);
    }

}
